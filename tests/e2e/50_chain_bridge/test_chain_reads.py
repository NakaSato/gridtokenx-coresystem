def test_get_signature_status_unknown_signature_not_found():
    """GetSignatureStatus for a syntactically-valid but never-submitted signature
    must return the structured 'NotFound' shape (service.rs:687-690): confirmed
    false, status 'NotFound'. Avoids needing a real prior tx. Request field:
    signature; response fields: confirmed, status, error."""
    if not _authorized():
        pytest.skip("auth mode mismatch")
    # A valid 64-byte all-zero Ed25519 signature in base58 (parses, never landed).
    zero_sig = "1" * 64  # base58 '1' == zero byte; 64 chars -> 64 zero bytes
    code, body = connect_call("GetSignatureStatus", {"signature": zero_sig}, ADMIN_ROLE)
    assert code == 200, f"GetSignatureStatus failed: {code} {body}"
    # Connect omits default false/empty; the not-found branch sets status NotFound
    # and leaves confirmed at its default (false -> omitted).
    assert body.get("status") == "NotFound", f"expected NotFound, got: {body}"
    assert body.get("confirmed", False) is False, f"unexpectedly confirmed: {body}"


