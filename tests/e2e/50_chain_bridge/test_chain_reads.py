"""Suite 50 — Chain Bridge previously-uncovered gRPC reads + the read-only
NATS `chain.tx.status` subject.

Complements `test_chain_bridge.py` (GetSlot/GetLatestBlockhash/GetBalance) and
`test_nats_tx.py` (submit/simulate write path). Here we exercise the read RPCs
that had no E2E coverage:

  - GetAccountData            (service.rs:499)
  - GetTokenAccountBalance    (service.rs:617)
  - GetRecentPrioritizationFees (service.rs:576)
  - GetEpochInfo              (service.rs:807)
  - GetSignatureStatus        (service.rs:655)
  - RequestAirdrop            (service.rs:732)  -- local validator only

…and the read-only NATS subject:

  - chain.tx.status           (consumer.rs:836 -> reply chain.tx.statusresult.{cid})

All proto method names + field casing verified against
`gridtokenx-blockchain-core/proto/chain_bridge.proto`. ConnectRPC/JSON renders
proto field names in lowerCamelCase, so request/response keys are camelCase
(e.g. `accountKeys`, `uiAmount`, `lastValidBlockHeight`); NATS envelope/result
JSON uses serde snake_case (matching `nats_schema.rs` and `test_nats_tx.py`).

Run: cd tests/e2e && uv run --no-project python -m pytest 50_chain_bridge/test_chain_reads.py -v
Skips gracefully if Chain Bridge / NATS / validator are unreachable, or for the
mainnet-only RequestAirdrop case.
"""
import os
import time
import uuid
from pathlib import Path

import pytest
import requests

GRPC = os.getenv("CHAIN_BRIDGE_GRPC", "localhost:5040")
SVC = "gridtokenx.chain.v1.ChainBridgeService"
ADMIN_ROLE = {"x-gridtokenx-role": "admin"}

# 11111… = the System program; exists on any cluster (proto GetAccountDataRequest.pubkey).
SYSTEM_PROGRAM = "11111111111111111111111111111111"

# Dev mTLS material generated by `just gen-certs` (mirrors test_chain_bridge.py).
ROOT = Path(__file__).resolve().parents[3]
CERT_DIR = ROOT / "infra" / "certs"
CA = CERT_DIR / "ca.crt"
CLIENTS = CERT_DIR / "clients"
# mTLS is active only when certs exist AND the bridge is not insecure — the dev stack
# ships certs but runs insecure HTTP (CHAIN_BRIDGE_INSECURE=true), so certs-on-disk alone
# must not force https (which would skip the whole suite against an http bridge).
MTLS = (
    CA.is_file()
    and (CLIENTS / "admin.crt").is_file()
    and os.getenv("CHAIN_BRIDGE_INSECURE", "").lower() != "true"
)

BASE = os.getenv("CHAIN_BRIDGE_HTTP", f"{'https' if MTLS else 'http'}://{GRPC}")

# meter-service is the only non-Admin role permitted to query chain.tx.status
# (consumer.rs:873). Its SPIFFE URI maps to ServiceRole::MeterService
# (gridtokenx-blockchain-core/src/auth.rs:137).
STATUS_IDENTITY = "spiffe://gridtokenx.th/prod/meter-service"


def _client_cert(name: str):
    return (str(CLIENTS / f"{name}.crt"), str(CLIENTS / f"{name}.key"))


def connect_call(method: str, body: dict, headers: dict = None,
                 timeout: float = 8.0, identity: str = "admin"):
    """Connect unary call -> (status_code, json|text). Same shape as
    test_chain_bridge.connect_call so both auth modes work."""
    h = {"Content-Type": "application/json"}
    if headers:
        h.update(headers)
    kwargs = {"json": body, "headers": h, "timeout": timeout}
    if MTLS:
        kwargs["verify"] = str(CA)
        if identity is not None:
            kwargs["cert"] = _client_cert(identity)
    r = requests.post(f"{BASE}/{SVC}/{method}", **kwargs)
    try:
        return r.status_code, r.json()
    except ValueError:
        return r.status_code, r.text


def _reachable() -> bool:
    try:
        connect_call("GetSlot", {}, ADMIN_ROLE, timeout=4)
        return True
    except requests.RequestException:
        return False


pytestmark = pytest.mark.skipif(
    not _reachable(),
    reason="Chain Bridge unreachable (down, or mTLS-only without infra/certs — "
           "run `just gen-certs`, or start with CHAIN_BRIDGE_INSECURE=true).",
)


def _authorized() -> bool:
    code, _ = connect_call("GetSlot", {}, ADMIN_ROLE)
    return code == 200


# --- gRPC read RPCs -------------------------------------------------------

def test_get_account_data_system_program():
    """GetAccountData for an existing account: exists=true, data present.
    Response fields: exists, data (proto GetAccountDataResponse, service.rs:524)."""
    if not _authorized():
        pytest.skip("server rejects admin identity; auth mode mismatch")
    code, body = connect_call("GetAccountData", {"pubkey": SYSTEM_PROGRAM}, ADMIN_ROLE)
    assert code == 200, f"GetAccountData failed: {code} {body}"
    # System program is an executable account -> exists must be true.
    assert body.get("exists") is True, f"system program not found: {body}"


def test_get_account_data_nonexistent_account():
    """GetAccountData for a never-funded account: exists=false (service.rs:532).
    Use a deterministic but unfunded pubkey (all 2s = address '111…2' family is
    unlikely funded on a fresh local validator)."""
    if not _authorized():
        pytest.skip("auth mode mismatch")
    # A valid base58 pubkey that is essentially never an existing account.
    never = "GTKxoNeXiStentAccountForE2eTestNeverFunded11"
    code, body = connect_call("GetAccountData", {"pubkey": never}, ADMIN_ROLE)
    # Either a structured invalid-pubkey error, or a 200 with exists=false.
    # ConnectRPC omits default (false) bools, so a missing `exists` == false.
    if code == 200:
        assert body.get("exists", False) is False, f"unexpected existing account: {body}"
    else:
        assert code != 200
        if isinstance(body, dict):
            assert body.get("code") or body.get("message"), f"unstructured error: {body}"


